# 2. Выполнить логические побитовые операции "И", "ИЛИ" и др.
# над числами 5 и 6. Выполнить
# над числом 5 побитовый сдвиг вправо и влево на два знака.

print(5 & 6) # побитовое "И"
print(5 | 6) # поитовое "ИЛИ"
print(5 ^ 6) # побитовое "исключающее ИЛИ"
print(5 << 2) # побитовый сдвиг влево
print(5 >> 2) # побитовый сдвиг вправо

